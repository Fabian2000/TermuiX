namespace TermuiX.Widgets;

/// <summary>
/// Specifies the visual style of a line widget.
/// </summary>
public enum LineType
{
    /// <summary>
    /// Solid single line (─ or │).
    /// </summary>
    Solid,

    /// <summary>
    /// Double line (═ or ║).
    /// </summary>
    Double,

    /// <summary>
    /// Thick line (━ or ┃).
    /// </summary>
    Thick,

    /// <summary>
    /// Dotted line (┈ or ┊).
    /// </summary>
    Dotted
}
