namespace TermuiX.Widgets;

/// <summary>
/// Specifies the horizontal alignment of text content.
/// </summary>
public enum TextAlign
{
    /// <summary>
    /// Text is aligned to the left.
    /// </summary>
    Left,

    /// <summary>
    /// Text is centered.
    /// </summary>
    Center,

    /// <summary>
    /// Text is aligned to the right.
    /// </summary>
    Right
}
